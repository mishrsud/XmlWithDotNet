namespace Common
{
	/// <summary>
	/// Represents a particular Position
	/// </summary>
	public class Position
	{
		/// <summary>
		/// Gets or sets the title.
		/// </summary>
		public string Title { get; set; }

		/// <summary>
		/// Gets or sets the employer.
		/// </summary>
		public string Employer { get; set; }

		/// <summary>
		/// Gets or sets the tenure.
		/// </summary>
		public string Tenure { get; set; }

		/// <summary>
		/// Gets or sets a value indicating whether this instance is current.
		/// </summary>
		public bool IsCurrent { get; set; }

		/// <summary>
		/// Gets or sets the role.
		/// </summary>
		public string Role { get; set; }

		/// <summary>
		/// Gets or sets the responsibilities.
		/// </summary>
		public string Responsibilities { get; set; }

		/// <summary>
		/// Gets or sets the achievements.
		/// </summary>
		public string Achievements { get; set; }

		/// <summary>
		/// Gets or sets the technologies.
		/// </summary>
		public string Technologies { get; set; }
	}
}
